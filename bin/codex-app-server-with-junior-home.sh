#!/bin/bash
# Launch Codex with Junior's isolated CODEX_HOME.
#
# This mirrors the environment used by the codex-app-server provider: generated
# config.toml, Junior-approved MCP servers only, symlinked auth.json, and
# JUNIOR_SPAWNED=1 so personal hooks do not participate in Slack-runner turns.

set -euo pipefail

parse_bool() {
  local name="$1" value="${2:-}" default="$3"
  if [ -z "$value" ]; then
    echo "$default"
    return
  fi
  local normalized
  normalized=$(echo "$value" | tr '[:upper:]' '[:lower:]' | sed 's/^[[:space:]]*//;s/[[:space:]]*$//')
  case "$normalized" in
    1|true|yes|on) echo "true"; return ;;
    0|false|no|off) echo "false"; return ;;
    *) echo "Error: Invalid $name: \"$value\" (expected true|false)" >&2; exit 1 ;;
  esac
}

ROOT="$(cd "$(dirname "$0")/.." && pwd)"
CODEX_HOME_PATH="${CODEX_ISOLATED_HOME_PATH:-$ROOT/data/codex-home}"
REAL_CODEX_HOME="${CODEX_REAL_HOME:-$HOME/.codex}"
MCP_ENABLED=$(parse_bool "CODEX_MCP_ENABLED" "${CODEX_MCP_ENABLED:-}" true)
SLACK_MCP=$(parse_bool "CODEX_SLACK_MCP_ENABLED" "${CODEX_SLACK_MCP_ENABLED:-}" true)
PLAYWRIGHT_MCP=$(parse_bool "CODEX_PLAYWRIGHT_MCP_ENABLED" "${CODEX_PLAYWRIGHT_MCP_ENABLED:-}" true)
MIXPANEL_MCP=$(parse_bool "CODEX_MIXPANEL_MCP_ENABLED" "${CODEX_MIXPANEL_MCP_ENABLED:-}" true)
MONGODB_MCP=$(parse_bool "CODEX_MONGODB_MCP_ENABLED" "${CODEX_MONGODB_MCP_ENABLED:-}" true)

mkdir -p "$CODEX_HOME_PATH"
if [ ! -e "$CODEX_HOME_PATH/auth.json" ] && [ -e "$REAL_CODEX_HOME/auth.json" ]; then
  ln -s "$REAL_CODEX_HOME/auth.json" "$CODEX_HOME_PATH/auth.json"
fi
rm -f "$CODEX_HOME_PATH/hooks.json"

{
  echo '# Generated by Junior. Do not add personal hooks, MCP servers, plugins, or skills here.'
  if [ -n "${CODEX_MODEL:-}" ]; then
    printf 'model = "%s"\n' "$CODEX_MODEL"
  fi
  echo 'approval_policy = "never"'
  echo 'sandbox_mode = "workspace-write"'
  echo

  if [ "$MCP_ENABLED" = "true" ] && [ "$SLACK_MCP" = "true" ]; then
    echo '[mcp_servers.slack-bot]'
    echo 'transport = "http"'
    echo 'url = "http://localhost:3456/mcp"'
    echo
  fi
  if [ "$MCP_ENABLED" = "true" ] && [ "$PLAYWRIGHT_MCP" = "true" ]; then
    echo '[mcp_servers.playwright]'
    echo 'command = "npx"'
    echo 'args = ["@playwright/mcp", "--headless"]'
    echo
  fi
  if [ "$MCP_ENABLED" = "true" ] && [ "$MIXPANEL_MCP" = "true" ]; then
    echo '[mcp_servers.mixpanel]'
    echo 'command = "npx"'
    echo 'args = ["-y", "mcp-remote", "https://mcp.mixpanel.com/mcp"]'
    echo
  fi
  if [ "$MCP_ENABLED" = "true" ] && [ "$MONGODB_MCP" = "true" ]; then
    echo '[mcp_servers.mongodb]'
    echo 'command = "npx"'
    echo 'args = ["-y", "mongodb-mcp-server@latest", "--readOnly"]'
    echo
  fi
  escaped_root="${ROOT//\\/\\\\}"
  escaped_root="${escaped_root//\"/\\\"}"
  printf '[projects."%s"]\n' "$escaped_root"
  echo 'trust_level = "trusted"'
  echo
} > "$CODEX_HOME_PATH/config.toml"

export CODEX_HOME="$CODEX_HOME_PATH"
export JUNIOR_SPAWNED=1

if [ "${1:-}" = "--interactive" ]; then
  shift
  exec codex "$@"
fi

exec codex app-server --listen stdio:// "$@"
