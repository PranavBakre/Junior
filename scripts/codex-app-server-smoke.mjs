#!/usr/bin/env node

import { spawn } from "node:child_process";
import { existsSync, mkdirSync, rmSync, symlinkSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { createInterface } from "node:readline";

const cwd = process.cwd();
const timeoutMs = Number(process.env.CODEX_APP_SMOKE_TIMEOUT_MS ?? "120000");
const isolatedHome = process.env.CODEX_ISOLATED_HOME_PATH
  ? resolve(process.env.CODEX_ISOLATED_HOME_PATH)
  : null;

if (isolatedHome) {
  mkdirSync(isolatedHome, { recursive: true });
  const realHome = process.env.CODEX_REAL_HOME ?? resolve(process.env.HOME ?? "", ".codex");
  const realAuth = resolve(realHome, "auth.json");
  const isolatedAuth = resolve(isolatedHome, "auth.json");
  if (!existsSync(isolatedAuth) && existsSync(realAuth)) {
    symlinkSync(realAuth, isolatedAuth);
  }
  rmSync(resolve(isolatedHome, "hooks.json"), { force: true });
  writeFileSync(
    resolve(isolatedHome, "config.toml"),
    [
      "# Generated by Junior app-server smoke.",
      "approval_policy = \"never\"",
      "sandbox_mode = \"workspace-write\"",
      "",
      "[mcp_servers.slack-bot]",
      "transport = \"http\"",
      "url = \"http://localhost:3456/mcp\"",
      "",
      `[projects.${JSON.stringify(cwd)}]`,
      "trust_level = \"trusted\"",
      "",
    ].join("\n"),
  );
}

const appServerArgs = ["app-server"];
if (process.env.CODEX_APP_SMOKE_DISABLE_HOOKS !== "false") {
  appServerArgs.push("--config", "hooks.enabled=false");
}
appServerArgs.push("--listen", "stdio://");

const proc = spawn("codex", appServerArgs, {
  cwd,
  stdio: ["pipe", "pipe", "pipe"],
  env: {
    ...process.env,
    ...(isolatedHome ? { CODEX_HOME: isolatedHome } : {}),
    JUNIOR_SPAWNED: "1",
    // Keep generated app-server analytics quiet for this protocol smoke.
    CODEX_APP_SMOKE: "1",
  },
});

let nextId = 1;
const pending = new Map();
const notifications = [];
const serverRequests = [];

function send(method, params) {
  const id = nextId++;
  const message = { jsonrpc: "2.0", id, method, params };
  proc.stdin.write(`${JSON.stringify(message)}\n`);
  return new Promise((resolve, reject) => {
    pending.set(id, { method, resolve, reject });
  });
}

function respond(id, result) {
  proc.stdin.write(`${JSON.stringify({ jsonrpc: "2.0", id, result })}\n`);
}

const stdout = createInterface({ input: proc.stdout });
stdout.on("line", (line) => {
  if (!line.trim()) return;
  let message;
  try {
    message = JSON.parse(line);
  } catch {
    console.log(JSON.stringify({ kind: "stdout.non_json", line }));
    return;
  }

  if ("id" in message && (message.result || message.error)) {
    const entry = pending.get(message.id);
    if (!entry) return;
    pending.delete(message.id);
    if (message.error) {
      entry.reject(new Error(`${entry.method}: ${JSON.stringify(message.error)}`));
    } else {
      entry.resolve(message.result);
    }
    return;
  }

  if ("id" in message && message.method) {
    serverRequests.push({ method: message.method, params: message.params });
    if (message.method.includes("requestApproval")) {
      respond(message.id, { approved: false });
    } else if (message.method === "item/tool/requestUserInput") {
      respond(message.id, { input: null });
    } else if (message.method === "mcpServer/elicitation/request") {
      respond(message.id, { action: "accept", content: {}, _meta: null });
    } else {
      respond(message.id, {});
    }
    return;
  }

  if (message.method) {
    notifications.push({ method: message.method, params: message.params });
  }
});

let stderr = "";
proc.stderr.on("data", (chunk) => {
  stderr += chunk.toString();
});

function waitFor(predicate, label, maxMs = timeoutMs) {
  const started = Date.now();
  return new Promise((resolve, reject) => {
    const timer = setInterval(() => {
      const value = predicate();
      if (value) {
        clearInterval(timer);
        resolve(value);
        return;
      }
      if (Date.now() - started > maxMs) {
        clearInterval(timer);
        reject(new Error(`Timed out waiting for ${label}`));
      }
    }, 100);
  });
}

async function main() {
  const initialize = await send("initialize", {
    clientInfo: {
      name: "junior-codex-app-smoke",
      title: "Junior Codex app-server smoke",
      version: "0.1.0",
    },
    capabilities: {
      experimentalApi: true,
      requestAttestation: false,
      optOutNotificationMethods: [],
    },
  });

  const marker = `JUNIOR_APP_SERVER_MARKER_${Date.now()}`;
  const threadStart = await send("thread/start", {
    cwd,
    model: process.env.CODEX_APP_SMOKE_MODEL ?? null,
    approvalPolicy: "never",
    sandbox: "read-only",
    baseInstructions: "You are a protocol smoke-test assistant.",
    developerInstructions: `For every response in this thread, include the exact marker ${marker}.`,
    ephemeral: false,
    experimentalRawEvents: false,
    persistExtendedHistory: false,
    threadSource: "user",
  });

  const threadId = threadStart.thread.id;
  const turnStart = await send("turn/start", {
    threadId,
    input: [
      {
        type: "text",
        text: "Reply with exactly: APP_SERVER_OK",
        text_elements: [],
      },
    ],
    approvalPolicy: "never",
  });

  await waitFor(
    () => notifications.find((n) => n.method === "turn/completed" && n.params.threadId === threadId),
    "first turn/completed",
  );

  const firstText = notifications
    .filter((n) => n.method === "item/agentMessage/delta" && n.params.threadId === threadId)
    .map((n) => n.params.delta)
    .join("");

  const longTurn = await send("turn/start", {
    threadId,
    input: [
      {
        type: "text",
        text: "Run a long command: sleep 30. Do not ask for approval.",
        text_elements: [],
      },
    ],
    approvalPolicy: "never",
    sandboxPolicy: {
      type: "workspaceWrite",
      writableRoots: [cwd],
      networkAccess: false,
      excludeTmpdirEnvVar: false,
      excludeSlashTmp: false,
    },
  });

  const longTurnId = longTurn.turn.id;
  await waitFor(
    () => notifications.find((n) => {
      if (n.method !== "item/started") return false;
      return n.params.threadId === threadId && n.params.turnId === longTurnId;
    }),
    "long turn item/started",
    30000,
  );

  await send("turn/interrupt", { threadId, turnId: longTurnId });
  await waitFor(
    () => notifications.find((n) => {
      if (n.method !== "turn/completed") return false;
      return n.params.threadId === threadId && n.params.turn.id === longTurnId;
    }),
    "interrupted turn/completed",
    30000,
  );

  const resume = await send("thread/resume", {
    threadId,
    cwd,
    approvalPolicy: "never",
    sandbox: "read-only",
    developerInstructions: `For every response in this thread, include the exact marker ${marker}.`,
    excludeTurns: true,
    persistExtendedHistory: false,
  });

  const resumeTurn = await send("turn/start", {
    threadId: resume.thread.id,
    input: [
      {
        type: "text",
        text: "Reply with exactly: RESUME_OK",
        text_elements: [],
      },
    ],
    approvalPolicy: "never",
  });

  await waitFor(
    () => notifications.find((n) => {
      if (n.method !== "turn/completed") return false;
      return n.params.threadId === threadId && n.params.turn.id === resumeTurn.turn.id;
    }),
    "resume turn/completed",
  );

  const optionalTurns = [];

  if (process.env.CODEX_APP_SMOKE_IMAGE_PATH) {
    const imageTurn = await send("turn/start", {
      threadId,
      input: [
        {
          type: "text",
          text: "Inspect the attached local image. Reply with IMAGE_OK, the marker, and a short description.",
          text_elements: [],
        },
        {
          type: "localImage",
          path: process.env.CODEX_APP_SMOKE_IMAGE_PATH,
        },
      ],
      approvalPolicy: "never",
    });
    await waitFor(
      () => notifications.find((n) => {
        if (n.method !== "turn/completed") return false;
        return n.params.threadId === threadId && n.params.turn.id === imageTurn.turn.id;
      }),
      "image turn/completed",
    );
    optionalTurns.push({ kind: "image", turnId: imageTurn.turn.id });
  }

  if (process.env.CODEX_APP_SMOKE_SLACK_CHANNEL_ID) {
    const text = process.env.CODEX_APP_SMOKE_SLACK_TEXT
      ?? `Codex app-server Slack MCP smoke ${marker}`;
    const slackTurn = await send("turn/start", {
      threadId,
      input: [
        {
          type: "text",
          text: [
            "Use the Slack MCP tool slack_send_message exactly once.",
            `Post this exact text to channel ${process.env.CODEX_APP_SMOKE_SLACK_CHANNEL_ID}:`,
            text,
            "After the tool call succeeds, reply with SLACK_MCP_OK and the marker.",
          ].join("\n"),
          text_elements: [],
        },
      ],
      approvalPolicy: "never",
    });
    await waitFor(
      () => notifications.find((n) => {
        if (n.method !== "turn/completed") return false;
        return n.params.threadId === threadId && n.params.turn.id === slackTurn.turn.id;
      }),
      "slack turn/completed",
      180000,
    );
    optionalTurns.push({ kind: "slack", turnId: slackTurn.turn.id, text });
  }

  const agentDeltas = notifications
    .filter((n) => n.method === "item/agentMessage/delta")
    .map((n) => n.params.delta)
    .join("");
  const completedTurns = notifications
    .filter((n) => n.method === "turn/completed")
    .map((n) => ({
      turnId: n.params.turn.id,
      status: n.params.turn.status,
      items: n.params.turn.items.map((item) => item.type),
    }));
  const itemTypes = notifications
    .filter((n) => n.method === "item/started" || n.method === "item/completed")
    .map((n) => ({
      method: n.method,
      type: n.params.item.type,
      status: n.params.item.status ?? null,
      command: n.params.item.command ?? null,
      server: n.params.item.server ?? null,
      tool: n.params.item.tool ?? null,
      arguments: n.params.item.arguments ?? null,
      error: n.params.item.error ?? null,
      result: n.params.item.result ?? null,
    }));
  const mcpNotifications = notifications
    .filter((n) => n.method.toLowerCase().includes("mcp"))
    .map((n) => ({ method: n.method, params: n.params }));

  console.log(JSON.stringify({
    initialize,
    threadId,
    sessionId: threadStart.thread.sessionId,
    marker,
    firstText,
    markerSeen: agentDeltas.includes(marker),
    isolatedHome,
    optionalTurns,
    completedTurns,
    itemTypes,
    mcpNotifications,
    notificationMethods: [...new Set(notifications.map((n) => n.method))].sort(),
    serverRequestMethods: [...new Set(serverRequests.map((r) => r.method))].sort(),
    serverRequests,
    stderr: stderr.trim().slice(0, 2000),
  }, null, 2));
}

const hardTimeout = setTimeout(() => {
  proc.kill("SIGKILL");
  console.error(`Smoke test timed out after ${timeoutMs}ms`);
  process.exit(124);
}, timeoutMs + 10000);

main()
  .catch((err) => {
    console.error(err.stack || err.message || String(err));
    process.exitCode = 1;
  })
  .finally(() => {
    clearTimeout(hardTimeout);
    proc.kill("SIGTERM");
  });
